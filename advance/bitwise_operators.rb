# Define Bitwise operators

a = 60
b = 30

# This is a Bianry AND operator
puts "This is a Binary And #{a&b}"

# This is a Binary OR operator
puts "This is a Binary OR #{a|b}"

# This is a Binary EOR opertor
puts "This is a Binary EOR #{a^b}"

# This is  ones complements operator
puts "This is a Binary Ones complementes #{~a}"

# This is a left shift operator
puts "This is a Binary Left shift #{a<<2}"

# This is a right shift operator
puts "This is a Binary Left shift #{a>>2}"